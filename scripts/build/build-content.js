/* ---------------- POLICY OUTPUT ---------------- */

const GENERATED_DIR = path.join(repoRoot, 'client', 'public', 'generated');

const TERMS_HTML_PATH = path.join(GENERATED_DIR, 'terms.html');
const PRIVACY_HTML_PATH = path.join(GENERATED_DIR, 'privacy.html');

const POLICY_START_MARKER =
  '<!-- POLICY_CONTENT_START (auto-generated by scripts/build/build-content.js) -->';

const POLICY_END_MARKER = '<!-- POLICY_CONTENT_END -->';

/* ---------------- HELPERS ---------------- */

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function ensureGeneratedDir() {
  if (!fs.existsSync(GENERATED_DIR)) {
    fs.mkdirSync(GENERATED_DIR, { recursive: true });
  }
}

function safeWriteFile(filePath, content) {
  if (!content || content.length < 200) {
    throw new Error(`Refusing to write suspiciously small file: ${filePath}`);
  }

  fs.writeFileSync(filePath, content, 'utf8');
}

/* ---------------- LOAD POLICY COPY ---------------- */

function tsToCjsModule(tsContent, exportName, interfaceRegex) {
  let js = tsContent
    .replace(interfaceRegex, '')
    .replace(/\nexport\s+const\s+/g, '\nmodule.exports = ')
    .replace(/\s*:\s*\w+\[\]\s*=/g, ' =');
  return js;
}

function loadTsAsCjs(tsPath, exportName) {
  const tsContent = fs.readFileSync(tsPath, 'utf8');
  const interfaceRegex = /export\s+interface\s+\w+\s*\{[^}]*\}\s*/g;
  const js = tsToCjsModule(tsContent, exportName, interfaceRegex);
  const tempPath = path.join(
    repoRoot,
    'scripts',
    'build',
    '.temp-' + exportName.toLowerCase() + '.cjs'
  );
  fs.writeFileSync(tempPath, js, 'utf8');
  try {
    const data = require(tempPath);
    return Array.isArray(data) ? data : (data && data[exportName]) || data;
  } finally {
    try {
      fs.unlinkSync(tempPath);
    } catch (_) {}
  }
}

function loadTermsCopy() {
  const data = loadTsAsCjs(TERMS_COPY_PATH, 'TERMS');
  return Array.isArray(data) ? data : (data && data.TERMS) || data;
}

function loadPrivacyCopy() {
  const data = loadTsAsCjs(PRIVACY_COPY_PATH, 'PRIVACY');
  return Array.isArray(data) ? data : (data && data.PRIVACY) || data;
}

/* ---------------- BUILD POLICY HTML ---------------- */

function buildPolicyHtml(sections, field) {
  return sections
    .filter((s) => s?.section && s?.[field])
    .map((s) => {
      const paragraphs = String(s[field])
        .split('\n\n')
        .map((p) => `<p>${p.trim()}</p>`)
        .join('\n');

      return `<h2>${s.section}</h2>\n${paragraphs}`;
    })
    .join('\n\n');
}

/* ---------------- HTML WRAPPER ---------------- */

function wrapPolicyPage(title, content) {
  return `<!doctype html>
<html lang="en">
<head>
<meta charset="UTF-8" />
<title>POWERBACK ${title}</title>
<meta name="viewport" content="width=device-width, initial-scale=1" />
<meta name="description" content="${title} for POWERBACK.us." />

<style>
body {
  max-width: 720px;
  margin: 40px auto;
  padding: 0 16px;
  font-family: system-ui, sans-serif;
  line-height: 1.6;
  color: #111;
}
</style>
</head>

<body>

<h1>${title}</h1>
<small>Last updated: March 2026</small>

${POLICY_START_MARKER}

${content}

${POLICY_END_MARKER}

</body>
</html>`;
}

/* ---------------- BUILD POLICY PAGES ---------------- */

function buildPolicyPages() {
  ensureGeneratedDir();

  const termsCopy = loadTermsCopy(),
    privacyCopy = loadPrivacyCopy();

  const termsSections = buildPolicyHtml(termsCopy, 'term'),
    privacySections = buildPolicyHtml(privacyCopy, 'privacy');

  const termsHtml = wrapPolicyPage('Terms of Service', termsSections),
    privacyHtml = wrapPolicyPage('Privacy Policy', privacySections);

  assert(termsCopy.length > 0, 'Terms tuples empty');
  assert(privacyCopy.length > 0, 'Privacy tuples empty');

  safeWriteFile(TERMS_HTML_PATH, termsHtml);
  safeWriteFile(PRIVACY_HTML_PATH, privacyHtml);

  console.log('✓ Generated legal pages');
}

